void solve()
{
    int n;
    cin >> n;
    vi v(n);
    for (int i = 0; i < n; i++)
    {
        cin >> v[i];
    }
    bool flag = true;
    for (int i = 0; i < n; i++)
    {
        if (v[i] != 0)
        {
            flag = false;
            break;
        }
    }
    if (flag)
    {
        cout << 0 << endl;
        return;
    }
    int xorr = 0;
    for (int x : v)
        xorr ^= x;
    if (xorr == 0)
    {
        cout << 1 << endl;
        cout << 1 << " " << n << endl;
        return;
    }
    else
    {
        if (n % 2 == 0)
        {
            cout << 2 << endl;
            cout << 1 << " " << n << endl;
            cout << 1 << " " << n << endl;
        }
        else
        {
            cout << 4 << endl;
            cout << 1 << " " << n << endl;
            cout << 1 << " " << n - 1 << endl;
            cout << n - 1 << " " << n << endl;
            cout << n - 1 << " " << n << endl;
        }
    }
}
int main()
{
    int t;
    cin >> t;
    while (t--)
    {
        solve();
    }
    return 0;
}
